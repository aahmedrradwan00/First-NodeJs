exports.getMonthlyPlan = async (req, res) => {
  try {
    const year = req.params.year * 1;
    const plan = await Tour.aggregate([
      {
        $unwind: '$startDates',
      },
      {
        $match: {
          startDates: {
            $gte: new Date(`${year}/01/01`),
            $lte: new Date(`${year}/12/31`),
          },
        },
      },
      {
        $group: {
          _id: { $month: '$startDates' },
          numTours: { $sum: 1 },
          tours: { $push: '$name' },
        },
      },
      {
        // to calc
        $addFields: { month: '$_id' },
      },
      {
        // 1 to show / 0 to hide
        $project: { _id: 0 },
      },
      {
        $sort: { numTours: -1 },
      },
      {
        $limit: 3,
      },
    ]);
    res.status(200).json({
      status: 'Success',
      data: {
        plan,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: 'Fail',
      message: err,
    });
  }
};
